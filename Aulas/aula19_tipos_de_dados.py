# Strings -> São textos (caracteres)


# Asps simples
print('Sergio Nibon')
print('Quando insiro aspas duplas com a simples, as duplas são evidenciadas -> ','Sergio "Nibon"', "\n")


# Asps Duplas
print("Sergio Nibon")
print('Quando insiro aspas simples com as duplas, as simples são evidenciadas -> ',"Sergio 'Nibon'", "\n")

# Escape
print("Sergio \"Nibon\"", "\n")

# r
print(r"Sergio \"Nibon\"")


